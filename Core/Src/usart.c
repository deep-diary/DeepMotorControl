void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart)
{
    /* Prevent unused argument(s) compilation warning */
    UNUSED(huart);
		// recieve data
		RxBuffer[rx_index++] = aRxBuffer;
		
		// recieved a frame
		if (rx_index >= 2 && RxBuffer[rx_index-2] == 0x0D && RxBuffer[rx_index-1] == 0x0A) {
				// resolve AT
				if (RxBuffer[0] == 'A' && RxBuffer[1] == 'T') {
						// solve CAN ID(4 byte)
						uart2can_frame.canID = (RxBuffer[2] << 24) | (RxBuffer[3] << 16) | (RxBuffer[4] << 8) | RxBuffer[5];
						
						// solve length(1 byte)
						uart2can_frame.data_length = RxBuffer[6];
						
						// resolve data
						for (int i = 0; i < uart2can_frame.data_length; i++) {
								uart2can_frame.data[i] = RxBuffer[7 + i];
						}
						
						packRdyFromUart = 1;  // package ready flag
						memset(RxBuffer, 0, rx_index);
				}
				
				// reset rx_index
				rx_index = 0;
		}

		// encase bigger then buffer size
		if (rx_index >= RXBUFFERSIZE) {
				rx_index = 0;  // reset
		}
		
	
    HAL_UART_Receive_IT(&huart1, (uint8_t *)&aRxBuffer, 1);
    /* NOTE: This function should not be modified, when the callback is needed,
       the HAL_UART_RxHalfCpltCallback can be implemented in the user file. */
}


